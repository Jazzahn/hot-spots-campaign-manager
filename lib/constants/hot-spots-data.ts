// Hot Spot contract reference data from Hot Spots: Draconis Reach
// All hot spots with their opposing contract pairs and accurate monthly track schedules.

export type ScheduleType = "fixed" | "roll-based" | "special";

export interface ScheduledTrack {
  trackType: string; // matches GENERAL_TRACKS names: "Recon", "Strike", etc.
  attackerSide: "A" | "B" | "winner" | "loser" | "conditional";
  note?: string;
}

export interface MonthSchedule {
  month: number;
  tracks: ScheduledTrack[]; // empty array = no tracks this month
  note?: string;
  isConditional?: boolean;
}

export interface RollTableEntry {
  roll: number;
  trackType: string | null; // null = no track on this result
  attackerSide: "A" | "B" | null;
}

export interface ContractTemplate {
  contractNumber: string;
  name: string;
  employer: string;
  contractType: string;
  terrain: string;
  planet: string;
  faction: string;
  durationMonths: number;
  basePayPct: number;
  basePayStep: number;
  supportType: string;
  supportPct: number;
  supportStep: number;
  transportPct: number;
  transportStep: number;
  salvageRightsPct: number;
  salvageStep: number;
  commandRights: string;
  commandStep: number;
  commandNonNegotiable: boolean;
  intensity: string;
  trackSequence: string;
  victoryCondition: string;
  specialRules: string;
  purchaseOptions: string;
  side: "A" | "B";
  optional?: boolean;
}

export interface HotSpotData {
  name: string;
  planet: string;
  primaryTerrain: string;
  faction: string;
  recommendedFor: "NEW_PLAYERS" | "EXPERIENCED" | "VETERAN_CAPSTONE";
  contracts: ContractTemplate[];
  scheduleType: ScheduleType;
  /** Month-by-month track schedule for fixed hot spots */
  monthlySchedule?: MonthSchedule[];
  /** Track selection table for roll-based hot spots */
  rollTable?: RollTableEntry[];
  /** How many dice to roll per month for roll-based hot spots */
  rollsPerMonth?: number;
  /** Victory condition notes for roll-based hot spots */
  rollVictoryNote?: string;
}


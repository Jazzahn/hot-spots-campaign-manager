export function getHotSpotByName(name: string): HotSpotData | undefined {
  return HOT_SPOTS_DATA.find((hs) => hs.name === name);
}

export function getContractTemplate(hotSpot: string, side: "A" | "B"): ContractTemplate | undefined {
  const hs = getHotSpotByName(hotSpot);
  return hs?.contracts.find((c) => c.side === side);
}

export function getAllContractTemplates(): ContractTemplate[] {
  return HOT_SPOTS_DATA.flatMap((hs) => hs.contracts);
}
